package hsp_Java;

public class EscapeCharacter {
    public static void main(String[] args){
        // \t : 一个制表位，实现对齐功能
        System.out.println("\t:制表字符");
        // \n : 换行输出
        System.out.println("\n:换行字符");
        // \\ : 一个斜杠
        System.out.println("\\:斜杆字符");
        // \" : 双引号
        System.out.println("\":双引号");
        // \' : 单引号
        System.out.println("\':单引号");
        // \r : 回车符
        System.out.println("这是符号是回车符\r:回车符");

    }
}
